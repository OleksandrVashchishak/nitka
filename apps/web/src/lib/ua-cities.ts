export type UaCity = (typeof UA_CITIES)[number];

function normalizeCityQuery(value: string) {
  return value
    .trim()
    .toLocaleLowerCase("uk-UA")
    .replace(/['’`]/g, "'")
    .replace(/\s+/g, " ");
}

export function filterUaCities(query: string, limit = 8): string[] {
  const q = normalizeCityQuery(query);
  if (!q) {
    return UA_CITIES.slice(0, limit);
  }

  const startsWith: string[] = [];
  const includes: string[] = [];

  for (const city of UA_CITIES) {
    const normalized = normalizeCityQuery(city);
    if (normalized.startsWith(q)) {
      startsWith.push(city);
    } else if (normalized.includes(q)) {
      includes.push(city);
    }
    if (startsWith.length >= limit) break;
  }

  return [...startsWith, ...includes].slice(0, limit);
}

export function isUaCity(value: string) {
  const q = normalizeCityQuery(value);
  return UA_CITIES.some((city) => normalizeCityQuery(city) === q);
}
