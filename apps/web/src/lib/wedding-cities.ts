/** Query-каталог лишаємо для UX-фільтрів (noindex). SEO ведемо на /vesillya/... */
export function vendorsByCityHref(cityName: string, category?: string) {
  const params = new URLSearchParams({ city: cityName });
  if (category) params.set("category", category);
  return `/vendors?${params.toString()}`;
}
